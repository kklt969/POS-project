    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        
        if(cbomerid.getSelectedIndex() == 0)
        {
            JOptionPane.showMessageDialog(this, "Select merchandise id");
        }
        
        else if(Checking.IsNull(txtitemname.getText()))
        {
             JOptionPane.showMessageDialog(this, "Enter Item name");
            
        }
        
        else
        {
            String st[] = new String[2];
            
            st[1] = txtitemname.getText();
            st[0] = cbomerid.getSelectedItem().toString();
            
            boolean br = mysql.isduplicate("itemdetail", st);
            
            if(br)
            {
                JOptionPane.showMessageDialog(this, "Duplicate record");
            }
            
            else
            {
                 String str[] = new String[6];
                 
                 str[0] = lblid.getText();
                 str[1] = cbomerid.getSelectedItem().toString();
                 str[2] = txtitemname.getText();
                 str[4] = txtremark.getText();
                 str[3]  = cursaleprice.getText().trim();
                 str[5] = txtquantity.getText().trim();
                 
                 boolean save = mysql.insertData("itemdetail",str);
                 if(save)
                 {
                     JOptionPane.showMessageDialog(this, "Sucessfully saved record");
                     autoid(); cbomerid.requestFocus();
                 }
                 
                 
            }
            
            
            
            
            
            
            
        }
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_saveActionPerformed

