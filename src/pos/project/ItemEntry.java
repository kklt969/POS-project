    public void fillMerchandise()
    {
        String str[] =mysql.getidForChoice("merchandise");
        
        for(int i = 0 ;  i<str.length; i++)
        {
            cbomerid.addItem(str[i].toString());
        }
    }
    
    
    
    
    
public void showBrandType()
{
    if(cbomerid.getSelectedIndex() == 0)
    {
        lblbrandname.setText("");lbltypename.setText("");
    }
    else
    {
        String result[] = mysql.getMerchandiseData(cbomerid.getSelectedItem().toString());
        System.out.println(Arrays.toString(result));
        
        
        String brandname = mysql.getBrandName(result[0]);
        lblbrandname.setText(brandname);
        String typename = mysql.getTypeName(result[1]);
        lbltypename.setText(typename);
    }
}
