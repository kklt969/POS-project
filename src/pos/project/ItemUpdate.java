    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        
         if(cboitemid.getSelectedIndex() == 0)
        {
            JOptionPane.showMessageDialog(this, "Please select item ID");
        
        }
         
         else if(Checking.IsNull(txtitemname.getText()))
         {
             JOptionPane.showMessageDialog(this, "Please enter item name ID");
         }
         
         else if(!Checking.checktxtprice(txtsaleprice.getText()))
         {
            txtsaleprice.requestFocus();
         }
         
         else if(Long.parseLong(txtsaleprice.getText())>saleprice +price)
         {
             JOptionPane.showMessageDialog(this, "Your sale price is too much");
         }
         else if(Long.parseLong(txtsaleprice.getText())<saleprice -price)
         {
             JOptionPane.showMessageDialog(this, "Your sale price is too low");
         }
         else
         {
            String str[] = new String[3]; str[0] = txtitemname.getText();
             str[1] = txtsaleprice.getText();
             str[2] = txtremark.getText();
             
             String id = cboitemid.getSelectedItem().toString();
             
             boolean save = mysql.updateRecord("itemdetail", id, str);
             
             if(save)
             {
                 JOptionPane.showMessageDialog(this, "Record successfully updated");
                 clear(); cboitemid.setSelectedIndex(0);
             }
             else
             {
                 JOptionPane.showMessageDialog(this, "failed to save record");
             }
             
         }
                
        
        
        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_updateActionPerformed

