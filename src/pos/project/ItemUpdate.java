    public void showitemdetail()
    {
        
        if(cboitemid.getSelectedIndex() == 0)
        {
            clear();
        
        }
        
        else
        {
            String result[] = mysql.getItemData(cboitemid.getSelectedItem().toString());
            
           txtitemname.setText(result[2]);
           txtsaleprice.setText(result[3]);
           saleprice = Long.parseLong(result[3]);
            price = (long)(saleprice*0.5);
            
            String strquery[] = mysql.getMerchandiseData(result[1]);
            
            String brandname = mysql.getBrandName(strquery[0]);
            lblbrandname.setText(brandname);
            
            String typename = mysql.getTypeName(strquery[1]);
            lbltypename.setText(typename);
            
            txtremark.setText(result[4]);
            
            
        }
        
        
        
    }
    
